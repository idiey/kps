import fs from 'node:fs';
import path from 'node:path';

const STALE_DAYS_DEFAULT = 14;

const args = new Set(process.argv.slice(2));

const staleDaysArg = process.argv.find((arg) => arg.startsWith('--stale-days='));
const staleDays = staleDaysArg
  ? Number(staleDaysArg.split('=')[1])
  : STALE_DAYS_DEFAULT;

const checkOnly = args.has('--check');
const writeOutput = args.has('--write') || !checkOnly;

const root = process.cwd();
const controlCenterDir = path.join(root, 'docs', '00-control-center');
const outputFile = path.join(controlCenterDir, '00-brain-index.md');

if (!fs.existsSync(controlCenterDir)) {
  console.error('Control Center not found at docs/00-control-center.');
  process.exit(1);
}

const orderedFiles = [
  'README.md',
  '01-project-brief.md',
  '02-current-state.md',
  '03-system-map.md',
  '04-decisions.md',
  '05-ai-agent-brief.md',
];

const allFiles = fs
  .readdirSync(controlCenterDir)
  .filter((file) => file.endsWith('.md') && file !== path.basename(outputFile));

const extraFiles = allFiles
  .filter((file) => !orderedFiles.includes(file))
  .sort((a, b) => a.localeCompare(b));

const files = [...orderedFiles.filter((f) => allFiles.includes(f)), ...extraFiles]
  .map((file) => {
    const filePath = path.join(controlCenterDir, file);
    const content = fs.readFileSync(filePath, 'utf8');
    const title = extractTitle(content) ?? file;
    const summary = extractSummary(content) ?? 'No summary available.';
    const lastUpdated = extractLastUpdated(content);
    const ageDays = lastUpdated ? calcAgeDays(lastUpdated) : null;
    const status = lastUpdated
      ? ageDays > staleDays
        ? 'Stale'
        : 'Fresh'
      : 'Unknown';

    return {
      file,
      title,
      summary,
      lastUpdated,
      ageDays,
      status,
    };
  });

const staleFiles = files.filter((f) => f.status === 'Stale');

if (writeOutput) {
  const output = buildIndex(files, staleFiles, staleDays);
  fs.writeFileSync(outputFile, output, 'utf8');
}

if (checkOnly) {
  if (staleFiles.length > 0) {
    console.error(`Stale files (${staleFiles.length}) exceed ${staleDays} days:`);
    for (const file of staleFiles) {
      console.error(`- ${file.file} (${file.lastUpdated}, ${file.ageDays} days)`);
    }
    process.exit(2);
  }
}

function extractTitle(content) {
  const lines = content.split(/\r?\n/);
  for (const line of lines) {
    if (line.startsWith('# ')) {
      return line.replace(/^#\s+/, '').trim();
    }
  }
  return null;
}

function extractSummary(content) {
  const lines = content.split(/\r?\n/);
  let seenTitle = false;
  let summaryLines = [];

  for (const line of lines) {
    if (!seenTitle) {
      if (line.startsWith('# ')) {
        seenTitle = true;
      }
      continue;
    }

    const trimmed = line.trim();

    if (!trimmed) {
      if (summaryLines.length > 0) {
        break;
      }
      continue;
    }

    if (
      trimmed.startsWith('##') ||
      trimmed.startsWith('- ') ||
      trimmed.startsWith('|') ||
      trimmed.startsWith('```') ||
      trimmed.startsWith('---')
    ) {
      if (summaryLines.length > 0) {
        break;
      }
      continue;
    }

    summaryLines.push(trimmed);
  }

  if (summaryLines.length === 0) {
    return null;
  }

  return summaryLines.join(' ');
}

function extractLastUpdated(content) {
  const match = content.match(/Last Updated\*\*:\s*(\d{4}-\d{2}-\d{2})/i) ||
    content.match(/Last Updated:\s*(\d{4}-\d{2}-\d{2})/i);
  if (!match) return null;
  const dateStr = match[1];
  return dateStr;
}

function calcAgeDays(dateStr) {
  const [year, month, day] = dateStr.split('-').map(Number);
  if (!year || !month || !day) return null;
  const dateUtc = new Date(Date.UTC(year, month - 1, day));
  const now = new Date();
  const todayUtc = new Date(Date.UTC(now.getUTCFullYear(), now.getUTCMonth(), now.getUTCDate()));
  const diffMs = todayUtc - dateUtc;
  return Math.floor(diffMs / 86400000);
}

function escapeCell(value) {
  return String(value ?? '')
    .replace(/\|/g, '\\|')
    .replace(/\r?\n/g, ' ')
    .trim();
}

function buildIndex(files, staleFiles, staleDays) {
  const now = new Date();
  const generatedOn = now.toISOString().slice(0, 10);

  const lines = [];
  lines.push('# Brain Index');
  lines.push('');
  lines.push('Single-page index for the Control Center. Generated by `node scripts/control-center-brain.mjs`.');
  lines.push('');
  lines.push('## Quick Links');
  for (const file of files) {
    lines.push(`- [${file.title}](${file.file})`);
  }
  lines.push('');
  lines.push('## Summaries');
  lines.push('| File | Summary |');
  lines.push('|------|---------|');
  for (const file of files) {
    lines.push(`| ${escapeCell(file.title)} | ${escapeCell(file.summary)} |`);
  }
  lines.push('');
  lines.push(`## Freshness (${staleDays}-day policy)`);
  lines.push('| File | Last Updated | Age (days) | Status |');
  lines.push('|------|--------------|------------|--------|');
  for (const file of files) {
    const ageText = file.ageDays === null ? 'Unknown' : String(file.ageDays);
    const updatedText = file.lastUpdated ?? 'Unknown';
    lines.push(`| ${escapeCell(file.title)} | ${escapeCell(updatedText)} | ${escapeCell(ageText)} | ${escapeCell(file.status)} |`);
  }
  lines.push('');
  lines.push('## Stale Files');
  if (staleFiles.length === 0) {
    lines.push(`- None. All files updated within ${staleDays} days.`);
  } else {
    for (const file of staleFiles) {
      lines.push(`- ${file.title} (${file.lastUpdated}, ${file.ageDays} days)`);
    }
  }
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push(`**Generated On**: ${generatedOn}`);
  lines.push(`**Stale Policy**: ${staleDays} days`);

  return lines.join('\n') + '\n';
}
